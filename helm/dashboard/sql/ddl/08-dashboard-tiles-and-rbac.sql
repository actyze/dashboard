-- 4. RBAC HELPER FUNCTIONS
-- =============================================================================

-- Function: Check if user has specific permission on dashboard
CREATE OR REPLACE FUNCTION nexus.user_has_dashboard_permission(
    p_user_id UUID,
    p_dashboard_id UUID,
    p_permission VARCHAR -- 'view', 'edit', 'delete', 'share'
) RETURNS BOOLEAN AS $$
DECLARE
    is_owner BOOLEAN;
    has_direct_permission BOOLEAN;
    has_group_permission BOOLEAN;
    is_admin BOOLEAN;
BEGIN
    -- 1. Check if user is owner (owners have all permissions)
    SELECT EXISTS (
        SELECT 1 FROM nexus.dashboards 
        WHERE id = p_dashboard_id AND owner_user_id = p_user_id
    ) INTO is_owner;
    
    IF is_owner THEN
        RETURN TRUE;
    END IF;
    
    -- 2. Check if user is ADMIN or SUPERADMIN (admins have all permissions)
    SELECT EXISTS (
        SELECT 1 FROM nexus.user_roles ur
        JOIN nexus.roles r ON ur.role_id = r.id
        WHERE ur.user_id = p_user_id 
        AND LOWER(r.name) IN ('admin', 'superadmin')
    ) INTO is_admin;
    
    IF is_admin THEN
        RETURN TRUE;
    END IF;
    
    -- 3. Check if dashboard is public and permission is 'view'
    IF p_permission = 'view' THEN
        IF EXISTS (
            SELECT 1 FROM nexus.dashboards 
            WHERE id = p_dashboard_id AND is_public = TRUE
        ) THEN
            RETURN TRUE;
        END IF;
    END IF;
    
    -- 4. Check direct user permission
    SELECT 
        CASE p_permission
            WHEN 'view' THEN can_view
            WHEN 'edit' THEN can_edit
            WHEN 'delete' THEN can_delete
            WHEN 'share' THEN can_share
            ELSE FALSE
        END
    INTO has_direct_permission
    FROM nexus.dashboard_permissions
    WHERE dashboard_id = p_dashboard_id 
        AND user_id = p_user_id
        AND (expires_at IS NULL OR expires_at > CURRENT_TIMESTAMP);
    
    IF has_direct_permission THEN
        RETURN TRUE;
    END IF;
    
    -- 5. Check group-based permission
    SELECT 
        CASE p_permission
            WHEN 'view' THEN can_view
            WHEN 'edit' THEN can_edit
            WHEN 'delete' THEN can_delete
            WHEN 'share' THEN can_share
            ELSE FALSE
        END
    INTO has_group_permission
    FROM nexus.dashboard_permissions dp
    JOIN nexus.user_groups ug ON dp.group_id = ug.group_id
    WHERE dp.dashboard_id = p_dashboard_id 
        AND ug.user_id = p_user_id
        AND (dp.expires_at IS NULL OR dp.expires_at > CURRENT_TIMESTAMP)
    LIMIT 1;
    
    RETURN COALESCE(has_group_permission, FALSE);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function: Get all dashboards accessible by user with permissions
CREATE OR REPLACE FUNCTION nexus.get_user_dashboards(p_user_id UUID)
RETURNS TABLE (
    dashboard_id UUID,
    title VARCHAR,
    description TEXT,
    owner_user_id UUID,
    owner_username VARCHAR,
    is_public BOOLEAN,
    is_favorite BOOLEAN,
    tags JSONB,
    tile_count BIGINT,
    can_view BOOLEAN,
    can_edit BOOLEAN,
    can_delete BOOLEAN,
    can_share BOOLEAN,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    last_accessed_at TIMESTAMP
) AS $$
BEGIN
    RETURN QUERY
    SELECT DISTINCT
        d.id,
        d.title,
        d.description,
        d.owner_user_id,
        u.username,
        d.is_public,
        d.is_favorite,
        d.tags,
        (SELECT COUNT(*) FROM nexus.dashboard_tiles dt WHERE dt.dashboard_id = d.id),
        nexus.user_has_dashboard_permission(p_user_id, d.id, 'view'),
        nexus.user_has_dashboard_permission(p_user_id, d.id, 'edit'),
        nexus.user_has_dashboard_permission(p_user_id, d.id, 'delete'),
        nexus.user_has_dashboard_permission(p_user_id, d.id, 'share'),
        d.created_at,
        d.updated_at,
        d.last_accessed_at
    FROM nexus.dashboards d
    LEFT JOIN nexus.users u ON d.owner_user_id = u.id
    WHERE 
        -- User is owner
        d.owner_user_id = p_user_id
        -- OR dashboard is public
        OR d.is_public = TRUE
        -- OR user has direct permission
        OR EXISTS (
            SELECT 1 FROM nexus.dashboard_permissions dp
            WHERE dp.dashboard_id = d.id 
                AND dp.user_id = p_user_id
                AND (dp.expires_at IS NULL OR dp.expires_at > CURRENT_TIMESTAMP)
        )
        -- OR user's group has permission
        OR EXISTS (
            SELECT 1 FROM nexus.dashboard_permissions dp
            JOIN nexus.user_groups ug ON dp.group_id = ug.group_id
            WHERE dp.dashboard_id = d.id 
                AND ug.user_id = p_user_id
                AND (dp.expires_at IS NULL OR dp.expires_at > CURRENT_TIMESTAMP)
        )
        -- OR user is admin/superadmin
        OR EXISTS (
            SELECT 1 FROM nexus.user_roles ur
            JOIN nexus.roles r ON ur.role_id = r.id
            WHERE ur.user_id = p_user_id 
                AND LOWER(r.name) IN ('admin', 'superadmin')
        )
    ORDER BY d.updated_at DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

