-- Dashboard Tiles and RBAC Enhancement
-- Creates a proper dashboard system with:
-- 1. Dashboard tiles (individual charts/widgets)
-- 2. RBAC permissions (view, edit, delete, share)
-- 3. Support for all Plotly.js chart types

SET search_path TO nexus, public;

-- =============================================================================
-- 1. DASHBOARD TILES TABLE
-- =============================================================================
-- Each dashboard can have multiple tiles (charts/widgets)

CREATE TABLE IF NOT EXISTS nexus.dashboard_tiles (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    dashboard_id UUID NOT NULL REFERENCES nexus.dashboards(id) ON DELETE CASCADE,
    
    -- Tile Content
    title VARCHAR(200) NOT NULL,
    description TEXT,
    
    -- Query Configuration
    sql_query TEXT NOT NULL,
    natural_language_query TEXT, -- Original NL query if generated by AI
    
    -- Chart Configuration
    chart_type VARCHAR(50) NOT NULL DEFAULT 'bar',
    chart_config JSONB, -- Plotly-specific config (colors, axes, etc.)
    
    -- Layout & Display
    position_x INTEGER NOT NULL DEFAULT 0, -- Grid position X
    position_y INTEGER NOT NULL DEFAULT 0, -- Grid position Y
    width INTEGER NOT NULL DEFAULT 6, -- Grid width (1-12)
    height INTEGER NOT NULL DEFAULT 4, -- Grid height
    
    -- Data Refresh
    refresh_interval_seconds INTEGER, -- Auto-refresh interval (null = manual only)
    last_refreshed_at TIMESTAMP,
    
    -- Metadata
    created_by UUID REFERENCES nexus.users(id) ON DELETE SET NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    -- Constraints
    CONSTRAINT valid_chart_type CHECK (chart_type IN (
        -- Basic Charts
        'bar', 'line', 'scatter', 'pie', 'donut',
        -- Statistical Charts
        'box', 'violin', 'histogram', 'histogram2d',
        -- 3D Charts
        'scatter3d', 'line3d', 'surface', 'mesh3d',
        -- Financial Charts
        'candlestick', 'ohlc', 'waterfall', 'funnel',
        -- Maps & Geo
        'scattergeo', 'choropleth', 'scattermapbox',
        -- Specialized
        'heatmap', 'contour', 'sankey', 'sunburst', 'treemap',
        'parallel', 'parcoords', 'table', 'indicator',
        -- Multi-series
        'area', 'stackedbar', 'groupedbar', 'bubble'
    )),
    CONSTRAINT valid_grid_position CHECK (position_x >= 0 AND position_y >= 0),
    CONSTRAINT valid_grid_size CHECK (width BETWEEN 1 AND 12 AND height >= 1)
);

-- Indexes for efficient queries
CREATE INDEX IF NOT EXISTS idx_dashboard_tiles_dashboard_id ON nexus.dashboard_tiles(dashboard_id);
CREATE INDEX IF NOT EXISTS idx_dashboard_tiles_created_by ON nexus.dashboard_tiles(created_by);
CREATE INDEX IF NOT EXISTS idx_dashboard_tiles_chart_type ON nexus.dashboard_tiles(chart_type);

-- Trigger for updated_at
DROP TRIGGER IF EXISTS set_timestamp_dashboard_tiles ON nexus.dashboard_tiles;
CREATE TRIGGER set_timestamp_dashboard_tiles 
    BEFORE UPDATE ON nexus.dashboard_tiles 
    FOR EACH ROW 
    EXECUTE PROCEDURE nexus.trigger_set_timestamp();

-- =============================================================================
-- 2. DASHBOARD PERMISSIONS TABLE (RBAC)
-- =============================================================================
-- Fine-grained access control for dashboards

CREATE TABLE IF NOT EXISTS nexus.dashboard_permissions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    dashboard_id UUID NOT NULL REFERENCES nexus.dashboards(id) ON DELETE CASCADE,
    
    -- Target (either user or group, not both)
    user_id UUID REFERENCES nexus.users(id) ON DELETE CASCADE,
    group_id UUID REFERENCES nexus.groups(id) ON DELETE CASCADE,
    
    -- Permissions (granular control)
    can_view BOOLEAN NOT NULL DEFAULT TRUE,
    can_edit BOOLEAN NOT NULL DEFAULT FALSE,
    can_delete BOOLEAN NOT NULL DEFAULT FALSE,
    can_share BOOLEAN NOT NULL DEFAULT FALSE, -- Can grant permissions to others
    
    -- Metadata
    granted_by UUID REFERENCES nexus.users(id) ON DELETE SET NULL, -- Who granted this permission
    granted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP, -- Optional expiration for temporary access
    
    -- Constraints
    CONSTRAINT either_user_or_group CHECK (
        (user_id IS NOT NULL AND group_id IS NULL) OR 
        (user_id IS NULL AND group_id IS NOT NULL)
    ),
    CONSTRAINT at_least_one_permission CHECK (
        can_view OR can_edit OR can_delete OR can_share
    ),
    -- Unique constraint: one permission record per user/group per dashboard
    CONSTRAINT unique_dashboard_user_permission UNIQUE (dashboard_id, user_id),
    CONSTRAINT unique_dashboard_group_permission UNIQUE (dashboard_id, group_id)
);

-- Indexes for efficient permission checks
CREATE INDEX IF NOT EXISTS idx_dashboard_permissions_dashboard ON nexus.dashboard_permissions(dashboard_id);
CREATE INDEX IF NOT EXISTS idx_dashboard_permissions_user ON nexus.dashboard_permissions(user_id);
CREATE INDEX IF NOT EXISTS idx_dashboard_permissions_group ON nexus.dashboard_permissions(group_id);
CREATE INDEX IF NOT EXISTS idx_dashboard_permissions_expires ON nexus.dashboard_permissions(expires_at) WHERE expires_at IS NOT NULL;

